CREATE PROCEDURE SP_ModificarPersonal
    @ID INT,
    @APELLIDO VARCHAR(50),
    @NOMBRE VARCHAR(50),
    @DNI VARCHAR(10),
	@FECHA_NACIMIENTO SMALLDATETIME,
    @MAIL VARCHAR(50),
